const bookNumber = urlParams.get('book-number');

console.log('검색된 책 번호:', bookNumber); // 책 번호가 제대로 받아졌는지 확인

// 책 번호로 책을 찾고 해당 책장의 번호를 강조
function searchBookByNumber(bookNumber) {
    const foundBooks = books.filter(b => b.book_number.toLowerCase().trim() === bookNumber.toLowerCase().trim());
    
    console.log('찾은 책들:', foundBooks); // 찾은 책 목록 확인

    if (foundBooks.length > 0) {
        document.getElementById('result-message').textContent = '책은 다음 책장에 있습니다.';

        // 모든 책장을 강조
        foundBooks.forEach(book => {
            highlightShelf(book.shelf_number);
        });
    } else {
        document.getElementById('result-message').textContent = '책을 찾을 수 없습니다.';
    }
}


// 책장 번호에 해당하는 책장 빨갛게 표시 (불이 들어오는 효과)
function highlightShelf(shelfNumber) {
    console.log('강조할 책장 번호:', shelfNumber); // 강조할 책장 번호 확인

    // 선택한 책장 번호 강조 (기존 강조 삭제 X)
    const selectedShelf = document.getElementById('shelf-' + shelfNumber);
    if (selectedShelf) {
        console.log('강조할 책장 찾음:', selectedShelf); // 강조할 책장이 제대로 찾았는지 확인
        selectedShelf.classList.add('highlight'); // 여러 개 강조 가능!
    } else {
        console.log('위치를 찾을 수 없습니다. ID:', 'shelf-' + shelfNumber);
    }
}


